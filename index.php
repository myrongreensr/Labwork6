	<h2>What is iCare Monitor</h2>
    <p>A new technology that is designed to help people keep closer tabs on their heart health, and can even be used to help identify atrial fibrillation (Afib) a serious medical condition that is a leading cause of stroke.</p>
    <h2>Special Design</h2>
    <p>iCare Monitor was thrust into the wearable spotlight via the Apple & Samsung Products. It's now finding it way into other smartwatches including the Samsung Galaxy Watch 3 and new Fitbit Sense health watch.</p>
  	<h2>History of Medical Wearables</h2>
    <p>Most of the wearables on the market at the moment have optical heart rate monitors inside them. This is a monitor that uses flashing LEDs to penetrate the skin and detect blood flow. When light is reflected off the flow of blood, it’s captured by the sensors. The algorithm then gets to work to produce heart rate data. </p>
    <h2>From a 500ft View</h2>
	<p>The key question for many people is 'why buy an smartwatch?'. Well, for many (dare we say most people) the feature is something you'll likely use once and then forget about. But for a large subset of people, iCarewatch is absoutely essential.

“Wearables are super high value for a small segment. It is probably going to be more advantageous to our users over 50 than 20 - 25 year olds, but we're trying to build something for everybody," Dr Conor Heneghan, Director of Research Algorithms at Fitbit told Wareable.</p>
